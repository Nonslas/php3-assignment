    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $q = $request->input('q');

        if ($q) {
            $users = User::where('cate_name', 'LIKE', "%$q%")->paginate(3);
        } else {
            $users = User::paginate(3);
        }

        return view('backend.users.index', [
            'users' => $users
        ]);
    }

